private void morph(double c){
    aTriangulation = new Triangulation();
    aTriangulation.rebuildFrom(sTriangulation);
    aTriangulation.shapeTo(dTriangulation, c);
    drawingPanel.clr();
    TextureMapper.mapTexture(
            sBuffer, 
            sTriangulation, 
            aTriangulation, 
            drawingPanel.getDBufferGraphics());
    drawingPanel.repaint();
}
    
private void frameSliderStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_frameSliderStateChanged
    JSlider source = (JSlider)evt.getSource();
    double c = (source.getValue() + 0.0) / source.getMaximum();
    morph(c);
}//GEN-LAST:event_frameSliderStateChanged

